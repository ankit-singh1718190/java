public class switchest {
    public static void main(String[] args) {
        int day=3;
        switch (day) {
            case 1:
            System.out.println("m");
                break;
            case 2:
            System.out.println("t");
                break;
            case 3:
            System.out.println("w"); 
                break;
            default:
            System.out.println("enyoy");       
        }
    }
}
